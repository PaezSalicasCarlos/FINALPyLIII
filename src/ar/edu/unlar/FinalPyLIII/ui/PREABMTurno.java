    private void jBtnNuevo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnNuevo1ActionPerformed

        if(jTxtDescripcion.getText().length()>2  &&  Integer.parseInt(jTxtCodigo.getText())>0)
        {
            t = new Turno();
            t.setCodigo(Integer.parseInt(jTxtCodigo.getText()));
            t.setDescripcion(jTxtDescripcion.getText());
            turnoDTO= new TurnoDTO( );
            if(!turnoDTO.extraerTodo().contains(t)){
                turnoDTO.crear(t);
                JOptionPane.showMessageDialog(this, "TURNO GENERADO");
            }
            else{
                JOptionPane.showMessageDialog(this, "YA EXISTE");
            }

            actualizaGrilla();
        }
        else{
            JOptionPane.showMessageDialog(null, "La Descripcion debe contener mas de 2 caracteres y el codigo debe ser distinto de cero");

        }

        limpiar();

    }//GEN-LAST:event_jBtnNuevo1ActionPerformed

    private void jBtnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminar1ActionPerformed
        int filaSelec= jTDatos.getSelectedRow();
        int a;
        Turno t = new Turno();
        TurnoDTO turnodto = new  TurnoDTO();

        if (filaSelec>=0)
        {
            a=(Integer) jTDatos.getValueAt(filaSelec,0);
            turnoGrillas.borrarFilas(jTDatos.getSelectedRow());
            t.setId(a);
            turnodto.eliminar(t);
            limpiar();
            actualizaGrilla();

        }
        else{
            JOptionPane.showMessageDialog(rootPane, "REGISTRO NO SELECCIONADO");
        }
    }//GEN-LAST:event_jBtnEliminar1ActionPerformed

    private void jBtnEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditar1ActionPerformed
        int filaSelec= jTDatos.getSelectedRow();
        int a;
        t = new Turno();
        turnoDTO= new  TurnoDTO();

        if (filaSelec>=0)
        {
            a=(Integer)jTDatos.getValueAt(filaSelec,0);

            if(jTxtDescripcion.getText().length()>2  &&  Integer.parseInt(jTxtCodigo.getText())>0)
            {
                t.setId(a);
                t.setCodigo(Integer.parseInt(jTxtCodigo.getText()));
                t.setDescripcion(jTxtDescripcion.getText());
                if(!turnoDTO.extraerTodo().contains(t)){
                    turnoDTO.modificar(t);
                    JOptionPane.showMessageDialog(this, "TURNO MODIFICADO");
                }
                else{
                    JOptionPane.showMessageDialog(this, "YA EXISTE");
                }

                actualizaGrilla();
            }
            else{
                JOptionPane.showMessageDialog(null, "La Descripcion debe contener mas de 2 caracteres y el codigo ser distinto de cero");

            }

            limpiar();
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "REGISTRO NO SELECCIONADO");
        }
    }//GEN-LAST:event_jBtnEditar1ActionPerformed

